package ControlFlow;

public class MaxOfThree {

	public static void main(String[] args) {
		int n1=10, n2=3, n3=78; 
		if(n1>=n2 && n1>=n3) {
			System.out.println("Gratest number is "+n1);
		}
		else if (n2>-n1 && n2>=n3) {
			System.out.println("Gratest number is "+n2);
		}
		else {
			System.out.println("Gratest number is "+n3);
		}

	}

}
